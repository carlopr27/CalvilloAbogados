/* ─── ENGINE ─────────────────────────────────────────────── */
function caSetLang(lang) {
  const t = CA_I18N[lang];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.dataset.i18n;
    if (t[k] !== undefined) el.innerHTML = t[k];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const k = el.dataset.i18nPh;
    if (t[k] !== undefined) el.placeholder = t[k];
  });
  const btn = document.getElementById('lang-toggle');
  if (btn) btn.innerHTML = lang === 'es'
    ? '🇺🇸 EN'
    : '🇲🇽 ES';
  localStorage.setItem('ca_lang', lang);
  document.documentElement.lang = lang === 'es' ? 'es' : 'en';
}

function caInitLang() {
  const saved = localStorage.getItem('ca_lang') || 'es';
  caSetLang(saved);
}

document.addEventListener('DOMContentLoaded', caInitLang);
